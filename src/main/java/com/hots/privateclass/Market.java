package com.hots.privateclass;

public class Market {

    private int wares_id;
    private String wares_name;
    private String wares_img;
    private int wares_price;
    private String wares_type;
    private String wares_from;

    public int getWares_id() {
        return wares_id;
    }

    public void setWares_id(int wares_id) {
        this.wares_id = wares_id;
    }

    public String getWares_name() {
        return wares_name;
    }

    public void setWares_name(String wares_name) {
        this.wares_name = wares_name;
    }

    public String getWares_img() {
        return wares_img;
    }

    public void setWares_img(String wares_img) {
        this.wares_img = wares_img;
    }

    public int getWares_price() {
        return wares_price;
    }

    public void setWares_price(int wares_price) {
        this.wares_price = wares_price;
    }

    public String getWares_type() {
        return wares_type;
    }

    public void setWares_type(String wares_type) {
        this.wares_type = wares_type;
    }

    public String getWares_from() {
        return wares_from;
    }

    public void setWares_from(String wares_from) {
        this.wares_from = wares_from;
    }
}
